#include "3-calc.h"

/**
 * op_add - Function to sum a and b
 * @a: The first parameter to add
 * @b: The second parameter to add
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function to calculate the difference of a and b
 * @a: The first parameter to calculate the difference
 * @b: The second parameter to calculate the difference
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function to calculate the product of a and b
 * @a: The first parameter to calculate the product
 * @b: The second parameter to calculate the product
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function to divide a and b
 * @a: First parameter to divide
 * @b: Second parameter to divide
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function to calculate the remainder of the division of a by b
 * @a: First parameter to divide
 * @b: Second parameter to divide
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
